    p En el entorno global, las acciones en eficiencia energética y su evolución hacia los sistemas de gestión de la energía constituyen un eje fundamental y decisivo en la reducción de emisiones de Gases de Efecto Invernadero (GEI) y la mitigación de los efectos del Cambio Climático. En tal sentido, su implementación se encuentra alineada con los Objetivos de Desarrollo Sostenible (ODS), y de manera principal con los de “Energía asequible y no contaminante”, “Ciudades y comunidades sostenibles” y “Producción y consumo responsables”, requiriendo personas con formación pertinente para su efectiva realización.  
